package model;

public class MedObject {

	String word;
	
	int med;

	public MedObject(String word, int med) {
		this.word = word;
		this.med = med;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public int getMed() {
		return med;
	}

	public void setMed(int med) {
		this.med = med;
	}
	
	
}
